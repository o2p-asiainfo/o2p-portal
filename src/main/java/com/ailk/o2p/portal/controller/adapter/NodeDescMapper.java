public class NodeDescMapper {

	private String nodeDescMapperId;
	private String srcNodeDescId;
	private String tarNodeDescId;
	private String contractAdapterId;
	private String actionTypeCd;
	public String getNodeDescMapperId() {
		return nodeDescMapperId;
	}
	public void setNodeDescMapperId(String nodeDescMapperId) {
		this.nodeDescMapperId = nodeDescMapperId;
	}
	public String getSrcNodeDescId() {
		return srcNodeDescId;
	}
	public void setSrcNodeDescId(String srcNodeDescId) {
		this.srcNodeDescId = srcNodeDescId;
	}
	public String getTarNodeDescId() {
		return tarNodeDescId;
	}
	public void setTarNodeDescId(String tarNodeDescId) {
		this.tarNodeDescId = tarNodeDescId;
	}
	public String getContractAdapterId() {
		return contractAdapterId;
	}
	public void setContractAdapterId(String contractAdapterId) {
		this.contractAdapterId = contractAdapterId;
	}
	public String getActionTypeCd() {
		return actionTypeCd;
	}
	public void setActionTypeCd(String actionTypeCd) {
		this.actionTypeCd = actionTypeCd;
	}
}
